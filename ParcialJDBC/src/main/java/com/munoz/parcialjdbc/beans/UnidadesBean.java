package com.munoz.parcialjdbc.beans;

public class UnidadesBean {

    public String cod_unidad_negocio;
    public String nom_unidad_negocio;
    public int nro_orden;

    public String getCod_unidad_negocio() {
        return cod_unidad_negocio;
    }

    public void setCod_unidad_negocio(String cod_unidad_negocio) {
        this.cod_unidad_negocio = cod_unidad_negocio;
    }

    public String getNom_unidad_negocio() {
        return nom_unidad_negocio;
    }

    public void setNom_unidad_negocio(String nom_unidad_negocio) {
        this.nom_unidad_negocio = nom_unidad_negocio;
    }

    public int getNro_orden() {
        return nro_orden;
    }

    public void setNro_orden(int nro_orden) {
        this.nro_orden = nro_orden;
    }
}
